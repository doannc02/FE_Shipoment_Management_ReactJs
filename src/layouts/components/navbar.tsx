function NavListMenu() {
  const [isMenuOpen, setIsMenuOpen] = React.useState(false);
  const [isMobileMenuOpen, setIsMobileMenuOpen] = React.useState(false);

  const renderItems = navListMenuItems.map(({ title, description }, key) => (
    <Link to="#" key={key}>
      <div className="flex items-center gap-3 rounded-lg p-2 hover:bg-gray-200">
        <div className="bg-blue-50 p-2 rounded-md">
          {/* Thêm biểu tượng nếu cần */}
          <span className="text-gray-900">Icon</span>
        </div>
        <div>
          <h6 className="text-sm font-bold text-gray-900">{title}</h6>
          <p className="text-xs font-medium text-gray-500">{description}</p>
        </div>
      </div>
    </Link>
  ));

  return (
    <div className="relative">
      <button
        onClick={() => setIsMenuOpen(!isMenuOpen)}
        className="flex items-center gap-2 py-2 px-4 text-gray-900 font-medium"
      >
        Resources
        <span
          className={`transition-transform transform ${
            isMenuOpen ? "rotate-180" : ""
          }`}
        >
          ▼
        </span>
      </button>
      {isMenuOpen && (
        <div className="absolute left-0 mt-2 w-48 bg-white shadow-lg rounded-lg">
          <div className="p-2">{renderItems}</div>
        </div>
      )}
      <div className="block lg:hidden">
        {/* Mobile menu */}
        <div
          className={`transition-all ${isMobileMenuOpen ? "block" : "hidden"}`}
        >
          {renderItems}
        </div>
      </div>
    </div>
  );
}

